# --------------------------------------------------------------------
# HMAC-SHA256 counter-mode + Fisher–Yates
# --------------------------------------------------------------------
def le32(i: int) -> bytes:
    return bytes((i & 0xFF, (i >> 8) & 0xFF, (i >> 16) & 0xFF, (i >> 24) & 0xFF))

def hmac_sha256(key: bytes, data: bytes) -> bytes:
    return hmac.new(key, data, hashlib.sha256).digest()

class CtrStream:
    def __init__(self, key: bytes, domain: str):
        self.key = key
        self.dom = domain.encode('utf-8')
        self.i = 0
        self.buf = b""
        self.pos = 0
        self.blocks_used = 0

    def _refill(self):
        self.buf = hmac_sha256(self.key, self.dom + le32(self.i))
        self.i += 1
        self.pos = 0
        self.blocks_used += 1

    def read_u64(self) -> int:
        out = 0
        for _ in range(8):
            if self.pos >= len(self.buf):
                self._refill()
            out = (out << 8) | self.buf[self.pos]
            self.pos += 1
        return out

def sbox_fisher_yates_unbiased(digits_ascii: bytes, domain: str):
    s = list(range(256))
    rng = CtrStream(digits_ascii, domain)
    for i in range(255, 0, -1):
        total = 1 << 64
        lim = (total // (i + 1)) * (i + 1)
        while True:
            x = rng.read_u64()
            if x < lim:
                j = x % (i + 1)
                break
        s[i], s[j] = s[j], s[i]
    inv = [0]*256
    for idx, v in enumerate(s):
        inv[v] = idx
    return s, inv, rng.blocks_used

def print_table_16x16(name: str, arr: List[int]):
    print(f"{name} (256 entries):")
    for r in range(16):
        row = arr[r*16:(r+1)*16]
        print("  " + " ".join(f"0x{v:02x}" for v in row))
    print()

