# main()
# Generate.
# --------------------------------------------------------------------
def main():
    pi_bytes  = PI_DEC_1024.encode('ascii')
    ln2_bytes = LN2_DEC_1024.encode('ascii')

    S_PI,  IS_PI,  blk_pi  = sbox_fisher_yates_unbiased(pi_bytes,  "TAU256/SBOX/PI/hmac-fy/v1")
    S_LN2, IS_LN2, blk_ln2 = sbox_fisher_yates_unbiased(ln2_bytes, "TAU256/SBOX/LN2/hmac-fy/v1")

    print_table_16x16("S_PI", S_PI)
    print_table_16x16("INV_S_PI", IS_PI)
    print_table_16x16("S_LN2", S_LN2)
    print_table_16x16("INV_S_LN2", IS_LN2)

    print("Usage (HMAC-SHA256 counter mode + FY unbiased):")
    print(f"  PI : hmac_blocks_used ≈ {blk_pi:4d}  bytes_read ≈ {blk_pi*32}")
    print(f"  LN2: hmac_blocks_used ≈ {blk_ln2:4d}  bytes_read ≈ {blk_ln2*32}")

if __name__ == "__main__":
    main()
